package businesslogiclayer;

/**
 * Class for coding a <code>String</code>.
 *
 * @author carbo
 */
public class Cypher {

    /**
     * Returns the encoded version of the passed string.
     *
     * @param string the string to encode
     * @return the encoded version of the passed string
     */
    public String encode(String string) {
        return string;
    }

    /**
     * Returns the decoded version of the passed string.
     *
     * @param string the string to decode
     * @return the decoded version of the passed string
     */
    public String decode(String string) {
        return string;
    }
}
